package sec02;

public class IntegerLiteralEx {

	public static void main(String[] args) {
		
		int val1 = 0b1011; // 2진수
		int val2 = 013; //8진수
		int val3 = 11;
		int val4 = 0xb3;
		
		System.out.println("val1: " + val1);
		System.out.println("val2: " + val2);
		System.out.println("val3: " + val3);
		System.out.println("val4: " + val4);
	}
}
