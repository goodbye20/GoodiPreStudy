package sec02;

public class EscapeExam {
	public static void main(String[] args) {
		
		System.out.println("번호\t이름\t직업");//탭만큼 뛰움
		System.out.println("번호\n이름\n직업");//개행
		System.out.println("번호\n이름\n직업");
		System.out.println("우리는\"개발자\"입니다.");//""삽입
		System.out.println("봄 \\ 여름 \\ 가을 \\ 겨울");// \삽입
	}
}
